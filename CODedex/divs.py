# Division (/), Floor Division (//), and Modulus (%) examples

print("Expression     | / (Division)  | // (Floor Division) | % (Modulus)")
print("---------------------------------------------------------------")

# Case 1
a, b = 10, 3
print(f"{a} / {b:<10} = {a / b:<12} | {a // b:<18} | {a % b}")

# Case 2
a, b = 9, 3
print(f"{a} / {b:<10} = {a / b:<12} | {a // b:<18} | {a % b}")

# Case 3
a, b = -10, 3
print(f"{a} / {b:<10} = {a / b:<12} | {a // b:<18} | {a % b}")

# Case 4
a, b = 5, 2
print(f"{a} / {b:<10} = {a / b:<12} | {a // b:<18} | {a % b}")
