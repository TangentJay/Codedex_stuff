'''
Comparison Operators
Used to compare values:
● == : Equal to
● != : Not equal to
● < : Less than
● > : Greater than
● <= : Less than or equal to
● >= : Greater than or equal to
'''
"""
Assignment Operators
Used to assign values to variables:
● = : Assigns value
● += : Adds and assigns
● -= : Subtracts and assigns
● *= : Multiplies and assigns
● /= : Divides and assigns
Example: x += 5 # Same as x = x + 5
"""

'''
Logical Operators
Used to combine conditional statements:
● and : True if both operands are true
● or : True if at least one operand is true
● not : Reverses the truth value
Example: if x > 5 and x < 10:
'''

"""
COntrol flow - conditionals statements
if statement, executes a block of code if a condition is true
elif statementConditional Statements
● if Statement
Executes a block of code if the condition is true.
● elif Statement
Short for "else if," checks another condition if the
previous one is false.
● else Statement
Executes if all preceding conditions are false.
"""

"""
Control flow -Loops
● for Loops
Iterates over a sequence (like a list, tuple, or string).
● while Loops
Repeats as long as a condition is true.
● break Statement
Exits the loop immediately.
● continue Statement
Skips the current iteration and continues with the next.

"""

